public class Calculator3 {
  void intSum(int x, int y) {
    int result = x + y;
    System.out.println("두 수의 덧셈은 " + result);
  }

  void doubleSum(double x, double y) {
    double result = x + y;
    System.out.println("두 수의 덧셈은 " + result);
  }

  void longSum(long x, long y) {
    long result = x + y;
    System.out.println("두 수의 덧셈은 " + result);
  }

  void floatSum(float x, float y) {
    float result = x + y;
    System.out.println("두 수의 덧셈은 " + result);
  }
}












